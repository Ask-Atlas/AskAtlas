package api

import (
	"net/http"
	"regexp"

	"github.com/Ask-Atlas/AskAtlas/api/pkg/apperrors"
)

var paramErrorRegex = regexp.MustCompile(`^parameter "([^"]+)"(?: in [^ ]+)? has an error: (.+)$`)

// OAPIValidatorErrorHandler is a custom ErrorHandler for the OpenAPI request validator middleware.
func OAPIValidatorErrorHandler(w http.ResponseWriter, message string, statusCode int) {
	var details map[string]string
	if statusCode == http.StatusBadRequest {
		matches := paramErrorRegex.FindStringSubmatch(message)
		if len(matches) == 3 {
			details = map[string]string{}
			field := matches[1]
			errMsg := matches[2]
			details[field] = errMsg
		} else {
			details = map[string]string{}
			details["validation"] = message
		}
	}

	err := &apperrors.AppError{
		Code:    statusCode,
		Status:  http.StatusText(statusCode),
		Message: "Invalid request parameters",
		Details: details,
	}
	apperrors.RespondWithError(w, err)
}

// OAPIStrictErrorHandler handles binding errors from the structural request parsing generated by oapi-codegen.
func OAPIStrictErrorHandler(w http.ResponseWriter, r *http.Request, err error) {
	details := map[string]string{}
	switch e := err.(type) {
	case *InvalidParamFormatError:
		details[e.ParamName] = e.Err.Error()
	case *UnmarshalingParamError:
		details[e.ParamName] = e.Err.Error()
	case *RequiredParamError:
		details[e.ParamName] = "is required"
	case *RequiredHeaderError:
		details[e.ParamName] = "is required"
	case *UnescapedCookieParamError:
		details[e.ParamName] = e.Err.Error()
	case *TooManyValuesForParamError:
		details[e.ParamName] = "expected one value"
	default:
		details["validation"] = err.Error()
	}

	appErr := &apperrors.AppError{
		Code:    http.StatusBadRequest,
		Status:  http.StatusText(http.StatusBadRequest),
		Message: "Invalid request parameters",
		Details: details,
	}
	apperrors.RespondWithError(w, appErr)
}
